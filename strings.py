#!/usr/bin/env python

print('the order of A is %d' % ord('A'))
print('the char of order 98 is %s' % chr(98))

a = 'hello world'
print('a = ', a)
print('a[:] = ', a[:])
print('a[::-1] = ', a[::-1])
print('a[0:] = ', a[0:])
print('a[0] = ', a[0])
print('a[-1] = ', a[-1])
print('len(a) = ', len(a))
print('a[1:3] = ', a[1:3])
print('a[6:-1] = ', a[6:-1])
print('a[6:] = ', a[6:])
print('a[6:-1:2] = ', a[6:-1:2]) 
print('a[:5:-1] = ', a[:5:-1])
